
def area(a):
    """
    Вычисляет площадь квадрата со стороной a.

    Площадь квадрата рассчитывается по формуле:
        S = a * a

    :param a: длина стороны квадрата (число).
    :return: площадь квадрата.
    """
    return a * a


def perimeter(a):
    """
    Вычисляет периметр квадрата со стороной a.

    Периметр квадрата рассчитывается по формуле:
        P = 4 * a

    :param a: длина стороны квадрата (число).
    :return: периметр квадрата.
    """
    return 4 * a
